#!/usr/bin/env bash
#
# priv-helper.sh - Minimal privileged helper for Sniffer USB Manager
#
# This script only allows a very narrow set of safe operations needed
# to unbind/bind USB devices from drivers and reload btusb.
#
# Intended to be called via sudo (with or without password).
#
# Usage from the GUI script:
#   sudo ./priv-helper.sh unbind 1-5:1.0 cdc_acm
#   sudo ./priv-helper.sh bind   1-8     btusb
#   sudo ./priv-helper.sh reload-btusb
#

set -euo pipefail

cmd=${1:-}
shift || true

case "$cmd" in
    unbind)
        iface=${1:-}
        driver=${2:-}
        if [[ -z "$iface" || -z "$driver" ]]; then
            echo "Usage: $0 unbind <interface> <driver>" >&2
            exit 1
        fi
        # Safety: only allow known driver names that we actually unbind from
        case "$driver" in
            btusb|cdc_acm|usb|usbserial|ftdi_sio|cp210x)
                echo "$iface" > "/sys/bus/usb/drivers/$driver/unbind" 2>/dev/null || true
                echo "Unbound $iface from $driver"
                ;;
            *)
                echo "Refusing to unbind from unknown driver: $driver" >&2
                exit 1
                ;;
        esac
        ;;

    bind)
        iface=${1:-}
        driver=${2:-btusb}
        if [[ -z "$iface" ]]; then
            echo "Usage: $0 bind <interface> [driver]" >&2
            exit 1
        fi
        echo "$iface" > "/sys/bus/usb/drivers/$driver/bind" 2>/dev/null || true
        echo "Bound $iface to $driver"
        ;;

    reload-btusb)
        modprobe -r btusb 2>/dev/null || true
        modprobe btusb 2>/dev/null || true
        echo "Reloaded btusb module"
        ;;

    modprobe)
        mod=${1:-}
        if [[ -z "$mod" ]]; then
            echo "Usage: $0 modprobe <module>" >&2
            exit 1
        fi
        # Only allow a whitelist of harmless modules for this tool
        case "$mod" in
            btusb|usbhid)
                modprobe "$mod" 2>/dev/null || true
                echo "modprobe $mod done"
                ;;
            *)
                echo "Refusing modprobe of $mod" >&2
                exit 1
                ;;
        esac
        ;;

    install-sudoers)
        if [[ $EUID -ne 0 ]]; then
            echo "install-sudoers must be run as root via pkexec or sudo" >&2
            exit 1
        fi

        real_user="${SUDO_USER:-}"
        if [[ -z "$real_user" || "$real_user" == "root" ]]; then
            if [[ -n "${PKEXEC_UID:-}" ]]; then
                real_user=$(getent passwd "$PKEXEC_UID" 2>/dev/null | cut -d: -f1 || true)
            fi
        fi
        if [[ -z "$real_user" || "$real_user" == "root" ]]; then
            real_user="${USER:-}"
        fi
        if [[ -z "$real_user" || "$real_user" == "root" ]]; then
            # Last resort: try logname or who
            real_user=$(logname 2>/dev/null || whoami 2>/dev/null || echo "")
        fi
        if [[ -z "$real_user" || "$real_user" == "root" ]]; then
            echo "Could not determine your (non-root) username for sudoers (SUDO_USER/PKEXEC_UID not set)" >&2
            exit 1
        fi

        script_dir="$(cd "$(dirname "$0")" && pwd)"
        helper_path="$script_dir/priv-helper.sh"
        rule_file="/etc/sudoers.d/sniffer-usb-manager"

        cat > "$rule_file" <<EOF
# Sniffer USB Manager - passwordless USB driver control
# Generated on $(date '+%Y-%m-%d %H:%M:%S')
# Safe: only allows the audited priv-helper.sh
$real_user ALL=(root) NOPASSWD: $helper_path
EOF

        chmod 0440 "$rule_file"

        if visudo -c -f "$rule_file" >/dev/null 2>&1; then
            echo "SUCCESS: Passwordless sudo installed for user '$real_user'"
            echo "You can now use the launcher without password prompts."
        else
            echo "ERROR: sudoers validation failed! Removing rule for safety." >&2
            rm -f "$rule_file"
            exit 1
        fi
        ;;

    remove-sudoers)
        if [[ $EUID -ne 0 ]]; then
            echo "remove-sudoers must be run as root" >&2
            exit 1
        fi
        rule_file="/etc/sudoers.d/sniffer-usb-manager"
        if [[ -f "$rule_file" ]]; then
            rm -f "$rule_file"
            echo "Passwordless sudo rule removed."
        else
            echo "No sudoers rule was present."
        fi
        ;;

    install-udev)
        if [[ $EUID -ne 0 ]]; then
            echo "install-udev must be run as root" >&2
            exit 1
        fi

        rules_file="/etc/udev/rules.d/99-sniffer-usb.rules"

        cat > "$rules_file" <<'UDEVEOF'
# Sniffer USB Manager - udev rules for Ubertooth One + nRF Sniffer (Raytac MDBT50Q-CX) + HackRF
# Generated by Sniffer USB Manager
# Supports unified management of your Ubertooth One + Raytac + HackRF setup.

# Give the user full access to the devices (no sudo needed for libusb tools)
SUBSYSTEM=="usb", ATTR{idVendor}=="1d50", ATTR{idProduct}=="6002", MODE="0666", GROUP="plugdev", TAG+="uaccess"
SUBSYSTEM=="usb", ATTR{idVendor}=="1d50", ATTR{idProduct}=="6003", MODE="0666", GROUP="plugdev", TAG+="uaccess"
SUBSYSTEM=="usb", ATTR{idVendor}=="1915", ATTR{idProduct}=="522a", MODE="0666", GROUP="plugdev", TAG+="uaccess"
SUBSYSTEM=="usb", ATTR{idVendor}=="1915", ATTR{idProduct}=="521f", MODE="0666", GROUP="plugdev", TAG+="uaccess"

# Also handle common alternative Nordic PIDs used with nRF Sniffer
SUBSYSTEM=="usb", ATTR{idVendor}=="1915", ATTR{idProduct}=="c00a", MODE="0666", GROUP="plugdev", TAG+="uaccess"

# HackRF One and variants (for unified RF device management alongside Ubertooth + Raytac)
SUBSYSTEM=="usb", ATTR{idVendor}=="1d50", ATTR{idProduct}=="6089", MODE="0666", GROUP="plugdev", TAG+="uaccess"
SUBSYSTEM=="usb", ATTR{idVendor}=="1d50", ATTR{idProduct}=="604b", MODE="0666", GROUP="plugdev", TAG+="uaccess"

# When these devices are plugged in, immediately unbind them from
# kernel drivers that fight with sniffing tools (btusb, cdc_acm).
# This makes the devices "ready to use" the moment you plug them in.
ACTION=="add", SUBSYSTEM=="usb", ATTR{idVendor}=="1d50", ATTR{idProduct}=="6002", \
    RUN+="/bin/sh -c 'echo %k > /sys/bus/usb/drivers/btusb/unbind 2>/dev/null || true; echo %k > /sys/bus/usb/drivers/cdc_acm/unbind 2>/dev/null || true'"

ACTION=="add", SUBSYSTEM=="usb", ATTR{idVendor}=="1915", ATTR{idProduct}=="522a", \
    RUN+="/bin/sh -c 'echo %k > /sys/bus/usb/drivers/btusb/unbind 2>/dev/null || true; echo %k > /sys/bus/usb/drivers/cdc_acm/unbind 2>/dev/null || true'"

ACTION=="add", SUBSYSTEM=="usb", ATTR{idVendor}=="1915", ATTR{idProduct}=="521f", \
    RUN+="/bin/sh -c 'echo %k > /sys/bus/usb/drivers/btusb/unbind 2>/dev/null || true; echo %k > /sys/bus/usb/drivers/cdc_acm/unbind 2>/dev/null || true'"
UDEVEOF

        chmod 0644 "$rules_file"
        # Reload rules
        udevadm control --reload-rules 2>/dev/null || true
        udevadm trigger 2>/dev/null || true

        echo "Udev rules installed: $rules_file"
        echo "Devices will now have easy permissions + auto-unbind from conflicting drivers on plug-in."
        ;;

    remove-udev)
        if [[ $EUID -ne 0 ]]; then
            echo "remove-udev must be run as root" >&2
            exit 1
        fi
        rules_file="/etc/udev/rules.d/99-sniffer-usb.rules"
        if [[ -f "$rules_file" ]]; then
            rm -f "$rules_file"
            udevadm control --reload-rules 2>/dev/null || true
            echo "Udev rules removed."
        else
            echo "No custom udev rules were present."
        fi
        ;;

    *)
        echo "Unknown command: $cmd" >&2
        echo "Allowed: unbind, bind, reload-btusb, modprobe, install-sudoers, remove-sudoers, install-udev, remove-udev" >&2
        exit 1
        ;;
esac
